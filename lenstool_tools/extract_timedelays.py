#!/usr/bin/env python
"""
Extract time delay surfaces from potential FITS files for each sample.

Computes Fermat potential-based time delays from lenstool potential maps
and outputs FITS files with time delay surfaces in days.

Time delay formula:
  tau = (1 + z_lens) * (D_l * D_s / (D_ls * c)) * (Fermat potential)
  
where Fermat potential = 0.5 * |theta - beta|^2 - psi(theta)

SOURCE.DAT COORDINATE INFERENCE
================================
This script supports inferring per-sample source plane coordinates from source.dat files
generated by lenstool. This enables higher accuracy time delay calculations where each
lens model uses its own inferred source position rather than a global fixed position.

How it works:
  1. source.dat files contain arcsecond OFFSETS from a reference position
  2. Reference coordinates are read from the corresponding PAR file (e.g., sample_00027_z1.524.par)
  3. Offsets are converted to absolute coordinates using: 
     abs_coord = ref_coord + offset_arcsec / 3600.0
  4. Multiple source positions (e.g., 1A, 1B, 1C) are combined via spherical mean
  5. The resulting source position is used for that sample's time delay calculation

Configuration requirements for source.dat inference:
  
  "source_plane_coords": "infer_from_source.dat"  <- Special value to enable inference
  
  "source_dat_directory": "output/poten/selected_output"  <- Directory containing .dat files
  
  "source_dat_object_id_mapping": {
    "A1": "1A",  <- Maps our image IDs to source.dat object IDs
    "A2": "1B",
    "A3": "1C"
  }
  
  "source_plane_coords_default": "11h53m19.6s +07d55m57s"  <- Fallback if source.dat unavailable

  File naming convention:
    - PAR file: sample_XXXXX_zZ.ZZZ.par (e.g., sample_00027_z1.524.par)
    - Source.dat: sample_XXXXX_zZ.ZZZ_source.dat (e.g., sample_00027_z1.524_source.dat)
  
  The PAR file is expected in "sample_input/" directory relative to execution directory.
  The source.dat file path is constructed from "source_dat_directory" config option.
"""

